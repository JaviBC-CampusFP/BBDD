DROP DATABASE IF EXISTS FIESTA;
CREATE DATABASE FIESTA;
USE FIESTA;
CREATE TABLE LOCALES (localesID int PRIMARY KEY AUTO_INCREMENT, Direccion varchar(25), Capacidad int, Horario char(11));
CREATE TABLE INVITADOS (invitadosID int PRIMARY KEY auto_increment, Nombre varchar(18), Apellido varchar(20), DNI char(9), TLF varchar(18));
CREATE TABLE CLIENTES (clienteID int PRIMARY KEY auto_increment, DNI char(9) NOT NULL, TLF varchar(18) NOT NULL, Nombre varchar(18) NOT NULL, Apellidos varchar(40) NOT NULL, Correo varchar(35) NOT NULL);

CREATE TABLE SESION (
sesionID int PRIMARY KEY AUTO_INCREMENT,
localesID int, invitadosID int, ClienteID int, fecha date, hora char(5),
foreign key (localesID) references locales(localesID),
FOREIGN KEY (invitadosID) REFERENCES invitados(invitadosID),
FOREIGN KEY (clienteID) REFERENCES clientes(clienteID)
);

